     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $kontak = Kontak::findOrFail($id);

        $request->validate([
            'title' => 'required|max:255',
            'description' => 'required',
        ]);

        $data = $request->all();

        try {
            $kontak->update($data);

            return redirect()->route('kontak.index')->with('success', 'Halaman Kontak berhasil diubah!');
        } catch(\Exception $e) {
            return redirect()->route('kontak.index')->with('errors', 'Halaman Kontak gagal diubah!');
        }
    }

