     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $quote = Quote::findOrFail($id);

        $request->validate([
            'title' => 'max:255',
            'description' => 'max:255'
        ]);

        $data = $request->all();

        try {
            $quote->update($data);

            return redirect()->route('quote.index')->with('success', 'Quote berhasil diubah!');
        } catch(\Exception $e) {
            return redirect()->route('quote.index')->with('errors', 'Quote gagal diubah!');
        }
    }

